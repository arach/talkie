/** Does the font have a glyph for this character (after uppercasing)? */
export function hasGlyph(char: string): boolean {
  return Object.prototype.hasOwnProperty.call(LED_FONT, char.toUpperCase());
}

/** Rows for a character (uppercased), falling back to a blank space glyph. */
export function glyphRows(char: string): string[] {
  return LED_FONT[char.toUpperCase()] ?? LED_FONT[" "];
}
